// String Method : A string holds characters in a sequence.
// There are many Sring methods in JavaScript.
// Some are mentioned below.

let Name = "Harshal"
// length of String
console.log(Name.length)

// String Case
console.log(Name.toUpperCase())
console.log(Name.toLowerCase())

// Slice Index
console.log(Name.slice(2,6))
console.log(Name.slice(2))

// Rerplacing
console.log(Name.replace("Har","Tri"))

